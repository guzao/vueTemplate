/**
 * 间隔时间
*/
export const enum IntervalTime {

    /** 一秒 */
    ONE_SECOND = 1000,

    /** 30秒 */
    THIRTY_SECOND = 1000 * 30,

    /** 一分钟 */
    ONE_MINIUTE = 1000 * 60,

    /** 二分钟 */
    TOW_MINIUTE = 1000 * 2 * 60,

    /** 三分钟 */
    THREE_MINIUTE = 1000 * 3 * 60,

    /** 四分钟 */ 
    FOUR_MINIUTE = 1000 * 4 * 60,

    /** 五分钟 */
    FIVE_MINIUTE = 1000 * 5 * 60,

    /** 十分钟 */
    TEN_MINIUTE = 1000 * 10 * 60,

    /** 二十分钟 */
    TWENTY_MILLI = 1000 * 20 * 60,
    
    /** 三十分钟 */
    THIRTY_MILLI = 1000 * 30 * 60,

    /** 一小时 */
    ONE_HOURS = 1000 * 60 * 60,

    /** 一天的毫秒数   */
    ONE_DAY = 24 * 60 * 60 * 1000
    

}

