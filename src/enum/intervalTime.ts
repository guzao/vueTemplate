/**
 * 间隔时间
*/
export const enum IntervalTime {

    /** 一秒 */
    ONE_SECOND = 1000,

    /** 30秒 */
    THIRTY_SECOND = 1000 * 30,

    /** 一分钟 */
    ONE_MINIUTE = 1000 * 60,

    /** 二分钟 */
    TOW_MINIUTE = 1000 * 2 * 60,

    /** 三分钟 */
    THREE_MINIUTE = 1000 * 3 * 60,

    /** 四分钟 */
    FOUR_MILLI_SECOND = 1000 * 4 * 60,

    /** 五分钟 */
    FIVE_MILLI_SECOND = 1000 * 5 * 60,

    /** 一小时 */
    ON_EHOURS = 1000 * 60 * 60

}